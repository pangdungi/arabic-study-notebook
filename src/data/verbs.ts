type VerbSeed = {
  id: string
  pastEnglish: string
  pastKorean: string
  past: readonly [string, string, string, string, string, string, string, string]
  pastPron: readonly [string, string, string, string, string, string, string, string]
  present: readonly [string, string, string, string, string, string, string, string]
  presentPron: readonly [string, string, string, string, string, string, string, string]
}

function buildVerb(seed: VerbSeed): VerbItem {
  const rows = verbPersonMeta.map((person, index) => ({
    id: person.id,
    label: person.label,
    hint: person.hint,
    past: { arabic: seed.past[index], pronunciation: seed.pastPron[index] },
    present: { arabic: seed.present[index], pronunciation: seed.presentPron[index] },
  }))

  return {
    id: seed.id,
    pastEnglish: seed.pastEnglish,
    pastKorean: seed.pastKorean,
    hePast: rows[3].past,
    rows,
  }
}

